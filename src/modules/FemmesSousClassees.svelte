    <h2>Les candidates dépassent les attentes</h2>
    <p>
    Comparons maintenant la position des candidat·es dans les listes avec leur classement en termes de voix de préférence. Nous considérons qu’un·e candidat·e était sous-classée si sa position dans la liste était plus basse que sa position en termes de voix de préférence. On peut l'interpréter comme une sous-estimation, de la part du parti, de la capacité du ou de la candidat·e à obtenir des voix. Dans le cas contraire, on parlera de sur-classement.
    </p>
    <p>
    Par exemple, si une candidate était 5ème sur la liste mais arrive 3ème en termes de voix, on dira qu’elle était sous-classée de 2 places.
    </p>
    <p>
    Nous avons calculé le sur- ou sous-classement moyen des hommes et des femmes, en excluant les candidat·es de fin de liste, pour qui la position ne peut pas être interprétée de la même manière.
    </p>
    <p>
    Sur l’ensemble des élections, on observe que les femmes ont tendance à être sous-classées, alors que les hommes ont tendance à être surclassés:
    </p>
    <p>
    
    
    
    <p>
    Le même phénomène s’observe à des degrés différents pour toutes les élections, à l’exception de la Communauté germanophone où l’inverse est observé.
