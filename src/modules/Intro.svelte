  <p>
    En 2024, plusieurs élections auront lieu : régionales, communautaires, fédérales et européennes en juin, suivies des communales en octobre. 
    </p>
    <p>
      Bien que cela puisse sembler encore loin, les partis politiques préparent déjà leurs listes électorales. La place des femmes sur ces listes constitue un enjeu central.
    </p>
    <p>
      On le sait, les femmes sont largement sous-représentées dans les différents parlements. 
      Par exemple, au Parlement Wallon, <a href="https://blog.robindevooght.be/posts/genre_au_parlement_wallon/">les hommes occupent 63% des sièges et 80% du temps de parole</a> 
      alors qu'ils ne constituent que 48% de la population en âge de voter. 
      Et, comme nous le verrons, cette inégalité découle, en partie au moins, de la place des femmes sur les listes électorales. 
    </p>
    <p>
      Pourtant, depuis 2002, <a href="https://igvm-iefh.belgium.be/fr/activites/politique/lois_sur_la_parite">les lois sur la parité</a> imposent aux partis politiques de présenter des listes où se trouvent autant d'hommes que de femmes, 
      et d’avoir un homme et une femme dans le duo de tête. 
      Avec des règles aussi strictes, comment la compositions des listes pourraient-elles encore défavoriser les femmes ?
    </p>
    <p>
    Pour le comprendre, plongeons-nous dans les élections précédentes et voyons comment, en 2019, les femmes ont été mises au second plan dans les listes électorales.
    </p>
</div>