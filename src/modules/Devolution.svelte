    <h3>Dévolution commençant par la 2ème place</h3>
    
    
    <p>
    Observons ce qu’il se passerait si les voix de dévolution étaient attribuées d’abord à la deuxième place, puis à la première. C’est un système complètement arbitraire qui n’aurait pas lieu d’être en pratique, mais il donne une indication de l’avantage que les têtes de listes obtiennent par le système de dévolution.
    </p>
    <p>
    Dans cette situation, <strong>les femmes obtiendraient 47,3% des sièges au lieu de 43,4%, avec 19 femmes élues de plus</strong>.
