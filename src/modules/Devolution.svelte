    <h2>Impact des voix de dévolution</h2>
    <p>
    Être positionné en tête de liste est un avantage en termes de visibilité pendant la campagne et dans les bureaux de vote, et joue à priori un rôle dans l’obtention d’un plus grand nombre de voix de préférences. Mais ce n’est pas le seul avantage d’une position en début de liste, en effet, la tête de liste sera la première personne à bénéficier du système de dévolution des voix.
    </p>
    <p>
    La dévolution des voix fonctionne à peu près comme ceci: la moitié des voix attribuées à la liste et non à des candidat·es spécifiques forme un pot. Les voix de ce pot sont attribuées à la tête de liste, jusqu’à ce qu’elle ait assez de voix pour être élue. S’il reste des voix dans le pot après cette opération, les voix restantes sont attribuées à la deuxième personne dans la liste, jusqu’à ce qu’elle atteigne le nombre de voix pour être élue, et ainsi de suite jusqu’à épuisement des voix.
    </p>
    <p>
    C’est ainsi que pour une liste ayant obtenu un seul siège, ce siège sera probablement donné à la tête de liste, même dans le cas où un·e autre candidat·e aurait obtenu plus de voix de préférence.
    </p>
    <p>
    Le système de dévolution a ses justifications, mais il n’est pas le seul système possible. Nous avons exploré ce qu’auraient donné les élections si d'autres systèmes de dévolution étaient appliqués. Nous n’avons donc pas touché aux voix de préférence obtenues, au nombre de voix pour la liste ou au nombre de sièges obtenus par liste.
    </p>
    <h3>Voix de préférences uniquement</h3>
    
    
