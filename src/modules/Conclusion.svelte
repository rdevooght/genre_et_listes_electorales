  <h2>La responsabilité des partis</h2>
  <p>
    Malgré des lois strictes sur la parité des listes électorales, 
    les hommes bénéficient souvent des meilleures places. 
    En plus de leur donner plus de visibilité pendant les campagnes, 
    cela joue un rôle déterminant au moment de l’attribution des sièges via le système de dévolution des voix.
  </p>

  <p>
    De nouvelles règles pourraient être mises en place, 
    comme l’obligation d’alterner entre hommes et femmes qui 
    <a href="https://www.lalibre.be/belgique/politique-belge/2020/11/20/la-tirette-sera-dapplication-pour-le-scrutin-regional-de-2024-a-bruxelles-2LHSV42QHBAW7BGJJ4QE3OYO3I/">sera d’application sur la région de Bruxelles-Capitale aux prochaines élections</a>. 
    Mais il y a aussi une responsabilité des partis politiques à faire de la parité une priorité lors de la conception des listes.
    Avec les élections de 2024 qui approchent, nous verrons bientôt quel parti aura pris cet enjeu au sérieux.
  </p>

  <p>D’autant plus que les résultats des candidates ont tendance, comme on l’a vu, à dépasser les attentes.</p>

