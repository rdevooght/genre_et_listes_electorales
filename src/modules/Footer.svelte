    <h3>Données et méthodologie</h3>

    <p>
      Les données de listes électorales et de votes proviennent de la 
      <a href="https://elections.fgov.be/elections-du-26-mai-2019-tableaux-des-resultats">Direction des Elections du SPF Intérieur</a>. 
      Ces données ont été compilées, reformatées pour être plus facilement analysables, et sont disponibles ici (lien à ajouter).
    </p>
    <p>
      Il n’existe pas de données publiques sur le genre ni sur le sexe légal des candidat·es. 
      En l’absence de celles-ci j’ai combiné une récolte manuelle de données, 
      et une estimation automatisée du genre basé sur les prénoms des candidats 
      et sur les données publiques de prénoms donnés à la naissance 
      <a href="https://statbel.fgov.be/fr/themes/population/noms-et-prenoms/prenoms-filles-et-garcons#figures">en Belgique</a> 
      et <a href="https://www.insee.fr/fr/statistiques/2540004#consulter">en France</a>.
    </p>
    <p>
      Le genre de près de 300 candidat·es a été vérifié manuellement (via la manière dont ils ou elles sont adressé·es dans les médias), 
      en particulier les candidat·es au places stratégiques ou ayant obtenu beaucoup de voix. 
      Pour les autres, le taux d’erreur est estimé à moins de 1%. 
      Les effets observés sont suffisamment importants pour que les conclusions ne soient pas affectées par ces erreurs.
    </p>
    <p>
      Si vous observez des erreurs dans les données ci-dessus, 
